const nginxConfigTemplate = `# Nginx configuration for SPA
server {
    listen 80;
    server_name localhost;
    root /usr/share/nginx/html;
    index index.html;

    # Gzip compression
    gzip on;
    gzip_types text/plain text/css application/json application/javascript text/xml application/xml text/javascript;

    # Cache static assets
    location ~* \\.(js|css|png|jpg|jpeg|gif|ico|svg|woff|woff2)$ {
        expires 1y;
        add_header Cache-Control "public, immutable";
    }

    # SPA fallback
    location / {
        try_files $uri $uri/ /index.html;
    }

    # Security headers
    add_header X-Frame-Options "SAMEORIGIN" always;
    add_header X-Content-Type-Options "nosniff" always;
    add_header X-XSS-Protection "1; mode=block" always;
}
`;

export default function DockerfileGenerator() {
  const [projectType, setProjectType] = useState<ProjectType>('node');
  const [showNginxConfig, setShowNginxConfig] = useState(false);
  const [output, setOutput] = useState('');
  const [config, setConfig] = useState<ConfigState>({
    port: '3000',
    appName: 'myapp',
    nodeVersion: '20',
    pythonVersion: '3.11',
    goVersion: '1.21',
    workDir: 'app',
    buildCommand: '',
    startCommand: 'index.js',
    envVars: '',
  });

  const generateDockerfile = () => {
    const template = dockerTemplates[projectType];
    if (template) {
      setOutput(template.template(config));
    }
  };

  const copyOutput = () => {
    navigator.clipboard.writeText(output);
  };

  const downloadFile = () => {
    const blob = new Blob([output], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = 'Dockerfile';
    a.click();
    URL.revokeObjectURL(url);
  };

  const downloadNginxConfig = () => {
    const blob = new Blob([nginxConfigTemplate], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = 'nginx.conf';
    a.click();
    URL.revokeObjectURL(url);
  };

  const updateConfig = (key: keyof ConfigState, value: string) => {
    setConfig(prev => ({ ...prev, [key]: value }));
  };

  return (
    <div style={{ display: 'grid', gap: '20px' }}>
      <Card title="Dockerfile 生成器">
        <p style={{ color: 'var(--text-secondary)', marginBottom: '16px' }}>
          选择项目类型，自动生成优化的 Dockerfile
        </p>

        <div style={{ marginBottom: '20px' }}>
          <div style={{ fontWeight: 600, marginBottom: '12px' }}>项目类型</div>
          <div style={{ display: 'grid', gridTemplateColumns: 'repeat(auto-fill, minmax(120px, 1fr))', gap: '8px' }}>
            {Object.entries(dockerTemplates).map(([key, value]) => (
              <button
                key={key}
                onClick={() => setProjectType(key as ProjectType)}
                style={{
                  padding: '12px',
                  border: 'none',
                  borderRadius: '8px',
                  cursor: 'pointer',
                  fontSize: '14px',
                  background: projectType === key
                    ? 'var(--accent-primary)'
                    : 'var(--bg-secondary)',
                  color: projectType === key
                    ? 'white'
                    : 'var(--text-primary)',
                  textAlign: 'left',
                }}
              >
                <div style={{ fontWeight: 600 }}>{value.name}</div>
              </button>
            ))}
          </div>
        </div>

        <div style={{ display: 'grid', gridTemplateColumns: 'repeat(2, 1fr)', gap: '16px', marginBottom: '20px' }}>
          <Input
            label="端口"
            value={config.port}
            onChange={e => updateConfig('port', e.target.value)}
            placeholder="3000"
          />
          <Input
            label="工作目录"
            value={config.workDir}
            onChange={e => updateConfig('workDir', e.target.value)}
            placeholder="app"
          />
        </div>

        {(projectType === 'node' || projectType === 'react' || projectType === 'vue' || projectType === 'nextjs') && (
          <div style={{ display: 'grid', gridTemplateColumns: 'repeat(2, 1fr)', gap: '16px', marginBottom: '20px' }}>
            <Select
              label="Node 版本"
              value={config.nodeVersion}
              onChange={e => updateConfig('nodeVersion', e.target.value)}
              options={[
                { value: '22', label: 'Node 22 (Latest)' },
                { value: '20', label: 'Node 20 (LTS)' },
                { value: '18', label: 'Node 18 (LTS)' },
                { value: '16', label: 'Node 16' },
              ]}
            />
            <Input
              label="启动文件"
              value={config.startCommand}
              onChange={e => updateConfig('startCommand', e.target.value)}
              placeholder="index.js"
            />
          </div>
        )}

        {projectType === 'python' && (
          <div style={{ marginBottom: '20px' }}>
            <Select
              label="Python 版本"
              value={config.pythonVersion}
              onChange={e => updateConfig('pythonVersion', e.target.value)}
              options={[
                { value: '3.12', label: 'Python 3.12 (Latest)' },
                { value: '3.11', label: 'Python 3.11' },
                { value: '3.10', label: 'Python 3.10' },
                { value: '3.9', label: 'Python 3.9' },
              ]}
            />
          </div>
        )}

        {projectType === 'go' && (
          <div style={{ marginBottom: '20px' }}>
            <Select
              label="Go 版本"
              value={config.goVersion}
              onChange={e => updateConfig('goVersion', e.target.value)}
              options={[
                { value: '1.22', label: 'Go 1.22 (Latest)' },
                { value: '1.21', label: 'Go 1.21' },
                { value: '1.20', label: 'Go 1.20' },
              ]}
            />
          </div>
        )}

        <div style={{ marginBottom: '20px' }}>
          <Input
            label="构建命令 (可选)"
            value={config.buildCommand}
            onChange={e => updateConfig('buildCommand', e.target.value)}
            placeholder="npm run build"
          />
        </div>

        <div style={{ marginBottom: '20px' }}>
          <div style={{ fontWeight: 600, marginBottom: '8px' }}>环境变量 (每行一个，格式: KEY=value)</div>
          <TextArea
            value={config.envVars}
            onChange={e => updateConfig('envVars', e.target.value)}
            placeholder="NODE_ENV=production&#10;API_URL=https://api.example.com"
            style={{ minHeight: '80px' }}
          />
        </div>

        <div style={{ display: 'flex', gap: '12px' }}>
          <Button variant="primary" onClick={generateDockerfile}>生成 Dockerfile</Button>
          {(projectType === 'react' || projectType === 'vue' || projectType === 'nginx') && (
            <Button variant="secondary" onClick={() => setShowNginxConfig(!showNginxConfig)}>
              查看 Nginx 配置
            </Button>
          )}
        </div>
      </Card>

      {output && (
        <Card title="Dockerfile">
          <div style={{ display: 'flex', gap: '8px', marginBottom: '12px' }}>
            <Button variant="secondary" size="sm" onClick={copyOutput}>复制</Button>
            <Button variant="primary" size="sm" onClick={downloadFile}>下载 Dockerfile</Button>
          </div>
          <pre className="result-box" style={{ margin: 0, maxHeight: '500px', overflow: 'auto' }}>
            {output}
          </pre>
        </Card>
      )}

      {showNginxConfig && (
        <Card title="nginx.conf (可选)">
          <div style={{ display: 'flex', gap: '8px', marginBottom: '12px' }}>
            <Button variant="secondary" size="sm" onClick={() => navigator.clipboard.writeText(nginxConfigTemplate)}>复制</Button>
            <Button variant="secondary" size="sm" onClick={downloadNginxConfig}>下载 nginx.conf</Button>
          </div>
          <pre className="result-box" style={{ margin: 0, maxHeight: '300px', overflow: 'auto' }}>
            {nginxConfigTemplate}
          </pre>
        </Card>
      )}
    </div>
  );
}