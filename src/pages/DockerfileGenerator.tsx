import { useState } from 'react';
import { Card, Button, TextArea, Input, Select } from '../components/common';

type ProjectType = 'node' | 'react' | 'vue' | 'nextjs' | 'python' | 'go' | 'java' | 'nginx';

interface DockerConfig {
  name: string;
  description: string;
  template: (config: ConfigState) => string;
}

interface ConfigState {
  port: string;
  appName: string;
  nodeVersion: string;
  pythonVersion: string;
  goVersion: string;
  workDir: string;
  buildCommand: string;
  startCommand: string;
  envVars: string;
}

